// 日本の祝日（予測が関わる 2026-07〜2027-03 の範囲）。予測カーブで週末同様の重みに扱う。
// 参考: 2026年 海の日7/20, 山の日8/11, 敬老9/21, 国民の休日9/22, 秋分9/23,
//       スポーツ10/12, 文化11/3, 勤労感謝11/23 / 2027年 元日1/1, 成人1/11, 建国2/11, 天皇誕生2/23, 春分3/21(振替3/22)
const JP_HOLIDAYS = new Set<string>([
  '2026-07-20', '2026-08-11', '2026-09-21', '2026-09-22', '2026-09-23',
  '2026-10-12', '2026-11-03', '2026-11-23',
  '2027-01-01', '2027-01-11', '2027-02-11', '2027-02-23', '2027-03-21', '2027-03-22',
]);

export function isHoliday(iso: string): boolean {
  return JP_HOLIDAYS.has(iso);
}
